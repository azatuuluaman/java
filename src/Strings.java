public class Strings {
    public static void main(String[] args) {
        int x =5;
         String s = "Hello";// String является классом, а классы пишутся с заглавной буквы
         String space = " ";           // Класс это чертеж/схема, объект это авто созданная с помощью чертежа
         String name = "Bob";
         System.out.println(s+space+name);
         System.out.println("Hello" + " "+"John");
        System.out.println("My number is " + x);
    }
}
